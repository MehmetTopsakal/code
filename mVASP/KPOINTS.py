def make_auto(filename,mk,dx,dy,dz):
    
    f = open(filename, 'w');
    
    f.write('Automatic mesh \n')
    f.write('0 \n')
    
    if (mk == 'M') or (mk == 'm'): f.write('M \n')
    if (mk == 'G') or (mk == 'g'): f.write('G \n')         
        
    f.write(str(int(dx)) + ' ' + str(int(dy)) + ' ' + str(int(dz)) + '\n')
    f.write('0 0 0 \n')    
    f.close()
    return


def make_lineh(filename,npts):
    
    f = open(filename, 'w');
    
    f.write('k-points along high symmetry lines       \n')
    f.write(str(int(npts)) + '                        \n')    
    f.write('Line-mode                                \n')
    f.write('Reciprocal lattice                       \n')
    f.write('                                         \n')
    f.write('0.33333333   0.333333333   0.0   ! K     \n')
    f.write('0.0   0.0   0.0   ! G                    \n')
    f.write('                                         \n')
    f.write('0.0   0.0   0.0   ! G                    \n')
    f.write('0.5   0.0   0.0   ! M                    \n')
    f.write('                                         \n')
    f.write('0.5   0.0   0.0   ! M                    \n')
    f.write('0.33333333   0.333333333   0.0   ! K     \n')
    f.close()
    return    



